#this program illustrate list and slicing

fruit=["apple","mango","grapes","oranges","blackberry","banana","cherry"]
print(fruit) 

print(fruit[0])
print(fruit[-7])          #print 7th element from end
 
print(fruit[6])
print(fruit[-1])         #print last element

print(fruit[:5])
print(fruit[3:])
print(fruit[3:6])
print(fruit[-3:])
print(fruit[:-2])